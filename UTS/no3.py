diagonal1 = float(input("Masukkan panjang diagonal 1: "))
diagonal2 = float(input("Masukkan panjang diagonal 2: "))

luas = (1/2) * diagonal1 * diagonal2

sisi1 = diagonal1 / 2
sisi2 = diagonal2 / 2
keliling = 2 * (sisi1 + sisi2)

print("Luas layang-layang: ", luas)
print("Keliling layang-layang: ", keliling)
